// Helper for string templates with {placeholder} syntax
window.tpl = function (str, vars) {
  if (typeof str !== 'string') return str;
  return str.replace(/\{(\w+)\}/g, function (_, k) {
    return vars && vars[k] != null ? vars[k] : '';
  });
};

